package bean;

public class Album {
	private int seq_alm;
	private String alm_name;
	private String alm_image;
	private int alm_price;
	private String alm_releasedate;
	private String alm_songwriter;
	private String alm_gerne;
	private int alm_type;
	
	
	public int getSeq_alm() {
		return seq_alm;
	}
	public void setSeq_alm(int seq_alm) {
		this.seq_alm = seq_alm;
	}
	public String getAlm_name() {
		return alm_name;
	}
	public void setAlm_name(String alm_name) {
		this.alm_name = alm_name;
	}
	public String getAlm_image() {
		return alm_image;
	}
	public void setAlm_image(String alm_image) {
		this.alm_image = alm_image;
	}
	public int getAlm_price() {
		return alm_price;
	}
	public void setAlm_price(int alm_price) {
		this.alm_price = alm_price;
	}
	public String getAlm_releasedate() {
		return alm_releasedate;
	}
	public void setAlm_releasedate(String alm_releasedate) {
		this.alm_releasedate = alm_releasedate;
	}
	public String getAlm_songwriter() {
		return alm_songwriter;
	}
	public void setAlm_songwriter(String alm_songwriter) {
		this.alm_songwriter = alm_songwriter;
	}
	public String getAlm_gerne() {
		return alm_gerne;
	}
	public void setAlm_gerne(String alm_gerne) {
		this.alm_gerne = alm_gerne;
	}
	public int getAlm_type() {
		return alm_type;
	}
	public void setAlm_type(int alm_type) {
		this.alm_type = alm_type;
	}
	
	

}
