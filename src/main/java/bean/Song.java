package bean;

public class Song {
	private int seq_song;
	private int seq_alm;
	private String song_name;
	private int song_mit;
	private int song_sec;
	
	public int getSeq_song() {
		return seq_song;
	}
	public void setSeq_song(int seq_song) {
		this.seq_song = seq_song;
	}
	public int getSeq_alm() {
		return seq_alm;
	}
	public void setSeq_alm(int seq_alm) {
		this.seq_alm = seq_alm;
	}
	public String getSong_name() {
		return song_name;
	}
	public void setSong_name(String song_name) {
		this.song_name = song_name;
	}
	public int getSong_mit() {
		return song_mit;
	}
	public void setSong_mit(int song_mit) {
		this.song_mit = song_mit;
	}
	public int getSong_sec() {
		return song_sec;
	}
	public void setSong_sec(int song_sec) {
		this.song_sec = song_sec;
	}
	
	

}
